// - Сохранение прогресса через Telegram Cloud Storage (опционально)
// - Поддержка всех режимов сложности (как в Яндекс Играх, но с Telegram-специфичным UI)
//
// UI адаптации:
// - Компактные карточки команд для мобильных
// - Swipe-жесты для навигации между группами/раундами
// - Telegram-стиль кнопок и цветов (можно использовать их брендинг)
